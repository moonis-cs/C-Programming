#define PERIC(r) (2*PI*r)
#define AREAC(r) (PI*r*r)
#define PERIS(x) (4*x)
#define AREAS(x) (x*x)
#define PERIT(x,y,z) (x+y+z)
#define AREAT(b,h) (0.5*b*h)
